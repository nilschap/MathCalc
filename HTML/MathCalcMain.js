
//Seiten mit Sinus berechnen
function Calcb(seitea,winkelb,winkela) {

    document.write("Solution: ");

    var solution = seitea*Math.sin(winkelb)/Math.sin(winkela);
    document.write(solution.toFixed(2));
}


